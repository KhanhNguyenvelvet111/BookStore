    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
        // TODO add your handling code here:
        if(PhoneIC.getText().isEmpty() ||
           AddressIC.getText().isEmpty() )
        JOptionPane.showMessageDialog(this, "Missing Infomation!");
        else{
            conn = ConnectionFactory.getConnection();
            try {
                
                String query = "update customers set CPhone = '"+PhoneIC.getText()+"',"
                        + " CAddress = '"+AddressIC.getText()+"',"
                        + "Gender = '"+inGender.getSelectedItem()+"' "
                        + "where CId = " + iduser;

                ps = conn.prepareStatement(query);
                st = ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer edited!");
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Unexpected exception!");
                st = -2;
            }
        }
    }//GEN-LAST:event_editBtnMouseClicked

