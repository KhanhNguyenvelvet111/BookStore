    private void DisplayHistory() {
        conn = ConnectionFactory.getConnection();
        //DefaultTableModel model = (DefaultTableModel) HistoryTable.getModel();
        //int myIndex = HistoryTable.getSelectedRow();
//        String Var1 = model.getValueAt(myIndex, 0).toString();
        String query = "SELECT orders.BillId , Title , orderdetails.BQuantity, books.Price,orderdetails.Subtotal , TotalPrice FROM orderdetails , orders , books, customers WHERE orders.BillId = orderdetails.BillId and orderdetails.BookId = books.BId and orders.CusId = customers.CId and CusId= " + iduser;
        try {
            ps = conn.prepareStatement(query);
            rs = ps.executeQuery(query);

            HistoryTable.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception ex) {
            Logger.getLogger(JF_InfoCus.class.getName()).log(Level.SEVERE, null, ex);
        }
        //get InfoCus
        String query2 = "SELECT * FROM customers WHERE CId =" + iduser;
        try {
            ps = conn.prepareStatement(query2);
            rs = ps.executeQuery(query2);
            if (rs.next()) {
                CIdIC.setText(rs.getString("CId"));
                NameIC.setText(rs.getString("CName"));
                MailIC.setText(rs.getString("CEmail"));
                PhoneIC.setText(rs.getString("CPhone"));
                AddressIC.setText(rs.getString("CAddress"));
                String gen = rs.getString("Gender");
                if(gen == "Female")
                    inGender.setSelectedIndex(2);
                else
                    inGender.setSelectedIndex(1);
                TypeDisIC.setText(rs.getString("TypeCus"));

            }

        } catch (Exception ex) {
            Logger.getLogger(JF_InfoCus.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

