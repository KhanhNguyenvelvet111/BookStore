    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)bookBillsTable.getModel();
        if(codeBookTB.getText().isEmpty() ||
                nameCusTB.getText().isEmpty()||
                dtm.getRowCount() == 0)
            JOptionPane.showMessageDialog(this, "Missing Infomation!");
        else{
            conn = ConnectionFactory.getConnection();
            try {
                //save orders
                String query1 = "INSERT INTO orders(BillId, CusId, DateCreate, TotalPrice, IsPaid, UId) "
                        + "VALUES (?,?,?,?,?,?)";
                ps = conn.prepareStatement(query1);
                String getIdBill = idTimeBills.getText();
                String billId = getIdBill.substring(4, 13);
                LocalDateTime localDateTime = LocalDateTime.now();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm:ss");
                String formattedDateTime = localDateTime.format(formatter);
                ps.setInt(1, Integer.parseInt(billId));
                ps.setInt(2, iduser);
                ps.setString(3, formattedDateTime);
                ps.setFloat(4, Float.parseFloat(totalPaymentLB.getText()));
                ps.setBoolean(5, true);
                ps.setInt(6, -1);
                st = ps.executeUpdate();
                
                //update type cus
                UpdateTypeCus(iduser);
                
                //save detail orders
                
                for(int i = 0; i < dtm.getRowCount();i++){
                    String query2 = "INSERT INTO orderdetails(BillId, BookId, BQuantity, Subtotal) "
                        + "VALUES (?,?,?,?)";
                    ps = conn.prepareStatement(query2);
                    ps.setInt(1, Integer.parseInt(billId));
                    ps.setInt(2, Integer.parseInt(dtm.getValueAt(i, 1).toString()));
                    ps.setInt(3, Integer.parseInt(dtm.getValueAt(i, 3).toString()));
                    ps.setFloat(4, Float.parseFloat(dtm.getValueAt(i, 5).toString()));
                    st = ps.executeUpdate();
                    
                    //update quantity book
                    int qtyBook1 = Integer.parseInt(dtm.getValueAt(i, 3).toString());
                    String query3 = "update books set Quantity = Quantity -" +qtyBook1+ " where BId = " + Integer.parseInt(dtm.getValueAt(i, 1).toString());
                    
                    ps = conn.prepareStatement(query3);
                    st = ps.executeUpdate();
                }
                
                JOptionPane.showMessageDialog(this, "Order saved!");
                ResetBook();
                DisplayBooks();
            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Unexpected exception!");
                st = -2;
            }
        }
        
        
        
        
    }//GEN-LAST:event_SaveMouseClicked

