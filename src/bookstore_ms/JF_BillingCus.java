    private void Payment(){
        subtotal = 0;
        //
        //settext payment
        //get discount
        DefaultTableModel dtm = (DefaultTableModel)bookBillsTable.getModel();
        if(dtm.getRowCount() != 0){
            String getdisc = discountCusTB.getText();
            int disc = 0;
            if(!getdisc.equals("0%")){
                disc = Integer.parseInt(getdisc.substring(0, 2).trim());
            }
            for(int l = 0; l< dtm.getRowCount(); l++){
                int getttPrice = Integer.parseInt(dtm.getValueAt(l, 5).toString());
                subtotal += getttPrice;
            }
            subtotalLB.setText(Float.toString(subtotal));
            float d = (float)disc /100;
            float d2 = subtotal * d;
            discountLB.setText(Float.toString(d2));
            totalpay = subtotal - d2;
            totalPaymentLB.setText(Float.toString(totalpay));
        }
    }
    
